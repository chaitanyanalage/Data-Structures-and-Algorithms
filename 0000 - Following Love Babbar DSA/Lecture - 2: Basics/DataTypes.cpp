#include<iostream>
using namespace std;

int main() {
    int a = 123;
    cout << "Integer: " << a << endl;

    char b = 'v';
    cout << "Character: " << b << endl;

    bool c = false;
    cout << "Boolean: " << c << endl;

    float d = 1.2;
    cout << "Float: " << d << endl;

    double e = 1.23;
    cout << "Double: " << e << endl;

}